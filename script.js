//add
function add(a, b) {
    return a + b
}

//subtraction
function subtract(a, b) {
    return a - b
}

//multiply
function multiply(a, b) {
    return a * b
}

//divide
function divide(a, b) {
    return a / b
}

console.log(add(4, 5));
console.log(subtract(5, 3));
console.log(multiply(4, 9));
console.log(divide(10, 2));